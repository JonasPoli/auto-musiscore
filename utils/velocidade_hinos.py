def bpm_do_hino(numero: int) -> int:
    """Retorna o BPM médio do hino."""
    return HINOS.get(numero, (70, 12))[0]

def intro_do_hino(numero: int) -> int:
    """Retorna a duração da introdução em segundos."""
    return HINOS.get(numero, (70, 12))[1]

def hino_mais_lento() -> tuple:
    """Retorna (numero, bpm) do hino com menor BPM."""
    return min(HINOS.items(), key=lambda x: x[1][0])

def hinos_por_velocidade(crescente=True) -> list:
    """Lista todos os hinos ordenados por BPM."""
    return sorted(HINOS.items(), key=lambda x: x[1][0], reverse=not crescente)

